package com.excelGetterSetter;

public class GroupGS {

	private String groupName;
	private String description;
	private String everyone;
	private String only_Staff;
	private String only_Residents;
	
	
	public String getGroupName() {
		return groupName;
	}
	public void setGroupName(String groupName) {
		this.groupName = groupName;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getEveryone() {
		return everyone;
	}
	public void setEveryone(String everyone) {
		this.everyone = everyone;
	}
	public String getOnly_Staff() {
		return only_Staff;
	}
	public void setOnly_Staff(String only_Staff) {
		this.only_Staff = only_Staff;
	}
	public String getOnly_Residents() {
		return only_Residents;
	}
	public void setOnly_Residents(String only_Residents) {
		this.only_Residents = only_Residents;
	}
	

	
	
	
}
